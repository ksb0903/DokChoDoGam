      if (usernameCheck.test(this.username)) {
        this.isUsernameChecked = true
      } else {
        this.isUsernameChecked = false
      }
    },
    checkPassword() {
      if (passwordCheck.test(this.password)) {
        this.isPasswordChecked = true
      } else {
        this.isPasswordChecked = false
      }
    },
    checkEmail() {
      if (emailCheck.test(this.email)) {
        this.isEmailChecked = true
      } else {
        this.isEmailChecked = false
      }
    },
    isUsernameDuplicate() {
      axios
        .get(BASE_URL + '/api/v1/user/auth/check/username/' + this.username)
        .then((res) => {
          if (res.data === false) {
            this.usernameDuplicate = false
            swal({
              title: '이 아이디는 사용하셔도 좋아요 😘',
              text: '👍👍👍👍👍',
              icon: 'success',
              buttons: false,
              timer: 1500
            })
          } else {
            this.usernameDuplicate = true
            swal({
              title: '이미 존재하는 아이디입니다😥',
              text: '👮‍♂️👮‍♀️👮‍♂️👮‍♀️👮‍♂️👮‍♀️',
              icon: 'warning',
              buttons: false,
              timer: 1500
            })
          }
        })
        .catch((err) => {
          console.log(err)
        })
    },
    isEmailDuplicate() {
      if (!emailCheck.test(this.email)) {
        swal({
          title: '정확한 이메일 주소인지 확인해주세요🙏',
          text: '🐯',
          icon: 'warning',
          buttons: false,
          timer: 1500
        })
      } else {
        axios
          .get(BASE_URL + '/api/v1/user/auth/check/email/' + this.email, {
            email: this.email
          })
          .then((res) => {
            if (res.data === false) {
              this.emailDuplicate = false
              swal({
                title: '이 이메일은 사용하셔도 좋아요 😘',
                text: '👍👍👍👍👍',
                icon: 'success',
                buttons: false,
                timer: 1500
              })
            } else {
              this.emailDuplicate = true
              swal({
                title: '이미 존재하는 이메일입니다😥',
                text: '👮‍♂️👮‍♀️👮‍♂️👮‍♀️👮‍♂️👮‍♀️',
                icon: 'warning',
                buttons: false,
                timer: 1500
              })
            }
          })
          .catch((err) => {
            console.log(err)
          })
      }
    },
    login() {
      axios
        .post(
          BASE_URL + '/api/v1/user/auth/login',
          {
            username: this.username,
            password: this.password
          },
          {
            headers: {
              'Content-type': 'application/json'
            }
          }
        )
        .then((result) => {
          localStorage.setItem('accessToken', result.data.accessToken)
          localStorage.setItem('refreshToken', result.data.refreshToken)
          const option = {
            headers: {
              AUTHORIZATION: 'Bearer ' + localStorage.getItem('accessToken')
            }
          }
          axios.get(BASE_URL + '/api/v1/user/myinfo', option).then((res) => {
            this.fetchUserInfo(res.data)
            if (res.data.newbie) {
              this.$router.replace({ name: 'setnickname' })
            } else {
              this.$router.replace({ name: 'main' })
            }
          })
        })
    },
    cancel() {
      this.$router.replace('/')
    },
    signup() {
      if (this.termsAgreed === false) {
        swal({
          title: '약관 동의를 먼저 진행해주세요🙏',
          text: '🐯',
          icon: 'warning',
          buttons: false,
          timer: 1500
        })
      } else if (this.usernameDuplicate === true) {
        swal({
          title: '아이디중복검사를 먼저 진행해주세요🙏',
          text: '🐯',
          icon: 'warning',
          buttons: false,
          timer: 1500
        })
      } else if (this.emailDuplicate === true) {
        swal({
          title: '이메일중복검사를 먼저 진행해주세요🙏',
          text: '🐯',
          icon: 'warning',
          buttons: false,
          timer: 1500
        })
      } else if (!passwordCheck.test(this.password)) {
        swal({
          title:
            '비밀번호는 영문자+숫자+특수문자 조합으로 8~25자리를 사용해야 해요🙏',
          text: '🐯',
          icon: 'warning',
          buttons: false,
          timer: 1500
        })
      } else if (!usernameCheck.test(this.username)) {
        swal({
          title: '아이디는 영문자나 숫자의 조합으로 5~20자리를 사용해야 해요🙏',
          text: '🐯',
          icon: 'warning',
          buttons: false,
          timer: 1500
        })
      } else if (!emailCheck.test(this.email)) {
        swal({
          title: '정확한 이메일 주소인지 확인해주세요🙏',
          text: '🐯',
          icon: 'warning',
          buttons: false,
          timer: 1500
        })
      } else if (this.password === this.password2) {
        axios
          .post(BASE_URL + '/api/v1/user/auth/signup', {
            email: this.email,
            nickname: this.nickname,
            password: this.password,
            region: this.region,
            username: this.username
          })
          .then((res) => {
            swal({
              title: '회원가입을 축하드립니다!🤗',
              text: '😸😸😸😸😸',
              icon: 'success',
              buttons: false,
              timer: 1500
            })
            this.login()
          })
          .catch((err) => {
            console.log(err)
          })
      } else {
        return swal({
          title: '비밀번호가 일치하지 않아요😥.',
          text: '🐯',
          icon: 'warning',
          buttons: false,
          timer: 1500
        })
      }
    }
  },
  watch: {
    username(newUsername) {
      this.usernameDuplicate = true
    },
    email(newEmail) {
      this.emailDuplicate = true
    }
  }
}
